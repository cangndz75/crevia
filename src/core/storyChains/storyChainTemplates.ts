import type {
  CreviaStoryChainStepHints,
  CreviaStoryChainStepTemplate,
  CreviaStoryChainTemplate,
} from './storyChainTypes';

function hints(
  mapHint: string,
  advisorHint: string,
  reportHint: string,
  socialHint: string,
  tomorrowHint: string,
  resultHint: string,
): CreviaStoryChainStepHints {
  return { mapHint, advisorHint, reportHint, socialHint, tomorrowHint, resultHint };
}

function step(
  stepKind: CreviaStoryChainStepTemplate['stepKind'],
  dayOffset: number,
  title: string,
  shortLine: string,
  eventFamilyIntent: string,
  variantBias: readonly string[],
  districtMemoryIntent: string,
  districtTrustIntent: string,
  hintBundle: CreviaStoryChainStepHints,
  relatedEventFamilyId?: string,
): CreviaStoryChainStepTemplate {
  return {
    stepKind,
    dayOffset,
    title,
    shortLine,
    eventFamilyIntent,
    variantBias,
    districtMemoryIntent,
    districtTrustIntent,
    relatedEventFamilyId,
    hints: hintBundle,
  };
}

