export function getStoryChainTemplateById(id: string): CreviaStoryChainTemplate | undefined {
  return STORY_CHAIN_TEMPLATES.find((template) => template.id === id);
}

export function getStoryChainTemplatesByKind(kind: string): CreviaStoryChainTemplate[] {
  return STORY_CHAIN_TEMPLATES.filter((template) => template.kind === kind);
}

export function getStoryChainTemplatesByDistrict(districtId: string): CreviaStoryChainTemplate[] {
  return STORY_CHAIN_TEMPLATES.filter((template) => template.districtIds.includes(districtId as never));
}

export function isKnownContentPackEventFamilyId(familyId: string): boolean {
  return STORY_CHAIN_TEMPLATES.some((template) => template.relatedEventFamilyIds.includes(familyId));
}
