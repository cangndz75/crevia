export const pilotEvents: EventCard[] = pilotEventsList.map((event) => ({
  ...event,
  decisions: event.decisions.map((d) => ({ ...d })),
}));

export function getPilotEvents(): EventCard[] {
  return pilotEvents.map((event) => ({
    ...event,
    decisions: event.decisions.map((d) => ({ ...d })),
  }));
}

export function getPilotEventsByDay(day: number): EventCard[] {
  return pilotEvents.filter((e) => e.day === day);
}

export function getPilotEventsByDistrict(
  districtId: PilotDistrictId,
): EventCard[] {
  return pilotEvents.filter(
    (e) => e.districtIds?.includes(districtId) ?? false,
  );
}

export function getPilotFallbackEvents(day: number): EventCard[] {
  return pilotEvents.filter((e) => e.day === day && e.fallback === true);
}
