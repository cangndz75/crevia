type PilotEventBase = {
  id: string;
  title: string;
  description: string;
  day: number;
  theme: PilotDayTheme;
  eventType: PilotEventType;
  districtIds: PilotDistrictId[];
  districtLabel: string;
  decisions: EventDecision[];
  riskLevel?: EventCard['riskLevel'];
  category?: string;
  contextTag?: string;
  urgencyHours?: number;
  previewEffects?: EventCard['previewEffects'];
  conditions?: EventCard['conditions'];
  priority?: number;
  fallback?: boolean;
  filterTags?: EventCard['filterTags'];
  delayHint?: boolean;
};

function pilotEvent(base: PilotEventBase): EventCard {
  return {
    id: base.id,
    title: base.title,
    category: base.category ?? 'Pilot Operasyon',
    riskLevel: base.riskLevel ?? 'medium',
    district: base.districtLabel,
    description: base.description,
    contextTag: base.contextTag ?? 'Pilot senaryo',
    urgencyHours: base.urgencyHours ?? 4,
    decisions: base.decisions,
    previewEffects: base.previewEffects ?? {
      publicSatisfaction: -5,
      risk: 8,
      xp: 16,
    },
    day: base.day,
    theme: base.theme,
    districtIds: base.districtIds,
    eventType: base.eventType,
    conditions: base.conditions,
    priority: base.priority,
    fallback: base.fallback,
    filterTags: base.filterTags,
    delayHint: base.delayHint,
  };
}

